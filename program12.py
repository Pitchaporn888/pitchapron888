print("โปรแกรมคำนวณพื้นที่")

print("1. สี่เหลี่ยมผืนผ้า")
print("2. สามเหลี่ยม")

เลือก = int(input("เลือกเมนู: "))

if เลือก == 1:
    กว้าง = float(input("ความกว้าง: "))
    ยาว = float(input("ความยาว: "))
    พื้นที่ = กว้าง * ยาว
    print("พื้นที่ =", พื้นที่)

elif เลือก == 2:
    ฐาน = float(input("ความยาวฐาน: "))
    สูง = float(input("ความสูง: "))
    พื้นที่ = 0.5 * ฐาน * สูง
    print("พื้นที่ =", พื้นที่)

else:
    print("เลือกเมนูไม่ถูกต้อง")